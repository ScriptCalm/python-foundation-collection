# four input statements
# \t is used for indentation style
print('Input:') # dividing in between input and output
my_name = str(input('My name is: '))
favourite_tv = str(input('\tMy favourite TV program is: '))
woke_up = input('\t\tI woke up at: ')
target_grade = input('\t\t\tMy target grade is: ')

# print statement showing the inputs using indentation
print('Output:') # dividing in between input and output
print(f'My name is {my_name}\n',
      f'\tMy favourite TV program is {favourite_tv}\n',
      f'\t\tI woke up at {woke_up}\n',
      f'\t\t\tMy target grade is {target_grade}')

