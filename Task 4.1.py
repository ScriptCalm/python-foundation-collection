# Input as a float
radius = float(input("Radius: "))

# Mathematical value of pi assigned to variable 'pi' directly
pi = 3.14

# Mathematical computation performed
area = pi * radius ** 2

# Print statement showing the output
# Round function used to limit the decimals to four digits
print("Area of the circle: ", round(area, 4))
