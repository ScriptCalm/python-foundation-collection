def maxFind(x, y, z): # define function
    list = [x, y, z]  # list for comparation 
    print(max(list))  # select and print the biggest no. from the list
                      # using max() function

# call the function with different numbers as arguments
maxFind(5, -3, 8)
maxFind(9, 10, 7)
maxFind(15, 4, 6)

